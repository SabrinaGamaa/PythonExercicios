n1 = float(input('Digite a primeira nota: '))
n2 = float(input('Digite a segunda nota: '))
n = (n1 + n2) / 2
print('A sua média foi {}'.format(n))
if n >=6.0:
    print('Parabéns você está na média!!!')
else:
    print('Infelizmente você não se esforçou e ficou abaixo da média.')
